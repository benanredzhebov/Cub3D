#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*current;
	t_list	*next;

	if (lst == NULL || *lst == NULL)
		return ;
	current = *lst;
	while (current != NULL)
	{
		next = current->next;
		if (del)
			del(current->content);
		free(current);
		current = next;
	}
	*lst = NULL;
}

/*
ft_lstclear deletes and frees the node and every
successor of that node, using the function del
and free.
Finally, the pointer to the list must be set to
NULL provides a  way to deallocate
*/