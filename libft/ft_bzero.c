#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*str;
	unsigned int	i;

	str = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		str[i] = 0;
		i++;
	}
}

/*
ft_bzero() writes n zeroed bytes to the string s.
If n is zero, bzero() does nothing.
This function is used to clear a memory 
area by setting it to 0.
*/