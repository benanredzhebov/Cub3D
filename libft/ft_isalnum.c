#include "libft.h"

int	ft_isalnum(int alnum)
{
	if ((alnum >= 'a' && alnum <= 'z')
		|| (alnum >= 'A' && alnum <= 'Z')
		|| (alnum >= '0' && alnum <= '9'))
		return (1);
	return (0);
}
